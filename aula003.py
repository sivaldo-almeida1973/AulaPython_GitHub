"""
Tipos float e int
int -> numeros inteiros (positivo ou negativo)
float -> numeros com ponto flutuante (negativo ou posittivo)

A funação type mostra o tipo que o python inferiu o valor
"""

#int -> numeros inteiros (positivo ou negativo)
print(11)
print(type(11))
print(-11)
print(type(-11))
print(0)


#float -> numeros com ponto flutuante (negativo ou posittivo)
print(1.10)
print(type(1.10))

print(-1.10)
print(type(-1.10))

#string
print(type('siavldo'))


#tipo boolean(true ou false)
#so existem duas respostas possíveis

print(10==10)
print(type(10 == 10))
print(10<10)
print(type(10 < 10))

